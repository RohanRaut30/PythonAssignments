#sort fruits

#ascending and descending using sorted keyword

f=["banana","apple","orange"]

#descending
r=sorted(f,reverse=True)
print(r)

#ascending
r=sorted(f,reverse=False)
print(r)

#ascending
m=["mango","banana","kiwi"]
m.sort()
print(m)

#set
s={"la","mi","si"}
k=sorted(s,reverse=True)
print(k)